// Abstraction - It is the process of hiding the internal complexities of how sth works
//  amd only exposing what's necessary for the user to interact with. e.g remote control

// Encapsulation - It is the bundling of data and methods that operate on that data into a single unit, 
// usually a class - and restricting direct access to some of the object's components. e.g Atm Machine

// Inheritance allows one class(child) to reuse the properties and methods of another class(parent), reducing
//  duplication and promoting reusuability.

// polymorphism means "many forms" - It allows objects of different classes to be treated as if they're of the
//  same type, but behave differently based on their class-specific implementations.

// composition - It is a design principle where one class contains or is composed of one or more objects of other 
// classes to reuse their functionability, instead of inheriting from them

// Summary
//  - Abstraction - Hide complexity, expose interface
//  - Encapsulation - Bundle + protect state
//  - Inheritance - Share logic across classes
//  - polymorphism - Same method, different behaviours
//  - Composition - Build by combining units


